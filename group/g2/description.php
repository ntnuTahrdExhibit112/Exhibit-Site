<h2>產品介紹</h2>
    <p class="indent1">
        <ul>
            <li>
                Face to Virtual ，以申請商管學院為目標的高三學生為研究對象，所設計的模擬面試的平台，有助於學生準備面試。<br>
                平台網址：<a class="group-page_link" href="https://facetovirtual.tk/index.php" target="_blank">https://facetovirtual.tk/index.php</a>
            </li>
            <li>
                基礎版分為題庫區及考試區，學生在題庫區觀看各類題目的解析，並在考試區進行考試後得到弱點分析及詳解參考。
                (如<a data-gallery="g2" class="portfolio-lightbox group-page_link" href="./g2/image/2.png">圖二</a>)
