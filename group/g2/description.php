<h2>發展動機</h2>
    <p class="indent1">
        <ul>
            <li>面試為申請大學入學的關鍵評分項目，然而高中生普遍缺乏面試經驗以及面試所需的基本技巧，對於面試應具備的態度與準備的內容也無所適從</li>
            <li>許多高中邀請大學教師能夠協助高中生進行推薦入學的「模擬面試」。基於高中端與大學端的學習落差，以及未必了解科系專業與個人的興趣，即使順利邀請大學教授到校進行「模擬面試」，單一次的面試或是短時間的面試，是否對於高中生有其實質的效用呢？</li>
        </ul>
    </p>
