<h2>Wingfolder天使折翼機</h2>
    <h4 class="indent1">產品介紹</h4>
        <p class="indent2">
            Wingfolder採用的方式為依據消費者想要使用的垃圾桶，為之客製化最上層卡板，協助整台機器可從垃圾桶洞口準確的放入與拿出，並於下方提供鉤子給予使用者勾住垃圾袋。<br>
            另外，由於機器需要電力才可運作，為求便利性，在設計上特別製作電池充電器，讓使用者可直接運用常見的Type C充電線完成充電。
        </p>
    <h4 class="indent1">內部結構設計</h4>
        <p class="indent2">
            主要使用壓克力、電子零件、內角鐵等材料結合雷切、3D列印、電子電路等技術製作而成。<br>
            而運作流程則為超音波感測完畢後，機器開始運轉，首先，板子往中間壓下，讓衛生棉進行第一次折疊，接著滾輪向內轉動後，讓衛生棉完成第二次折疊，並順勢掉入下方垃圾桶中。<br>
            而在機器外觀上，除了在中間採用H型的結構，同時也在外部設計成籃子式的造型，僅露出使用者需要用到的部分，讓整體機器更加穩固，且較不易受損害。
        </p>
