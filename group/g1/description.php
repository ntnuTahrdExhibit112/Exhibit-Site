<h2>設計理念</h2>
    <p class="indent1">
        1. 衛生棉之沾血面將摺疊至內側，經血不會露出<br>
        2. 使用衛生棉本身特性，不消耗額外材料<br>
        3. 密封垃圾桶，以減少異味<br>
        4. 女性方便使用<br>
        5. 清潔人員可快速並乾淨的收取垃圾
    </p>
