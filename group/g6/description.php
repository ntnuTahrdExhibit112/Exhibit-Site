<p>
    本專題起初以自組機械手臂為出發點，運用系上電子電路課程所學和Arduino、mblock製作出以機電整合為主題的互動式展覽作品，3月底於科教館「智慧夢工廠」常設展中展出，希望透操作實體機械手臂及我們設計的教學影片和問答遊戲，將機電整合的概念以簡單的方式讓社會大眾了解，並引起大眾對工程領域的興趣。<br>
