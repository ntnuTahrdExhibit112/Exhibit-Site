<p>
    此互動式展覽作品完成後，結合組員們未來的發展，我們將作品與國三生活科技課程「認識電與控制的應用」單元結合，發展成2節課的教案，於10/06及10/25分別到龍門國中及福和國中進行總共3個國三班級的試教，第一次嘗試了生生用平板，第二次汲取第一次的經驗在教學方式作了大幅度調整，並將AR融入教學；經過試教我們深刻體會到在製作作品時，根據不同的情境會需要不斷修正調整，驗證了實作的重要性。<br>
