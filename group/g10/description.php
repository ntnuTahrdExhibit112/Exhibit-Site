<h2>Deciverse遊戲說明</h2>
    <p class="indent1">
        透過在虛擬世界的三個不同主題企業兌換現實世界的貨幣，成為贏家吧。<br>
        玩家需要透過在宇宙銀行存放二、八、或十六進位制貨幣，轉換成十進位貨幣。<br>
        回合中，也可以透過達成目標取得NFT，使用功能，或是透過在宇宙銀行使用駭客技能幣，來銷毀或掠奪他人資產，贏得遊戲，成為虛擬及真實世界財富之王！
    </p>