                <h3>系所特色</h3>
                <p class="fst-italic p-5"> 科技系原屬培育中學「生活科技」教師之專門機構，然為因應師培市場之萎縮，科技系於十年前即著手調整課程結構，以培育學生朝向科技產業界發展為目標。 <br>
                    目前，科技系的課程本質，是以科技產業為主體，學生藉由各科技產業課程，例如傳播科技業、製造科技業、營建科技業、運輸科技業，來瞭解台灣科技社會的結構與內涵。在上課的型態上，則是提供學生以「設計」及「製作」的方式來進行，藉以深入瞭解科技產業之工作內涵與程序。 <br>
                    在此基礎之下，本系再輔以人力資源發展的專業課程，使學生具備「科技產業知識」及「人力資源發展專長」，得以進入科技產業從事企業的教育訓練工作。 </p>
